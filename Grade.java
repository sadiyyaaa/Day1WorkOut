public class Grade {
public static void main(String[] args) {
    int marks=85;
    if(marks>=90){
        System.out.println("Grade is A");
    }
    else if(marks>=75){
        System.out.println("Grade is B");
    }
    else if(marks>=50){
        System.out.println("Grade is C");
    }
    else{
        System.out.println("Grade is Failed");
    }

}
    
}
